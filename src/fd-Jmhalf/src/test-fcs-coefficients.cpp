    // Всюду сходящийся ряд
    BmpVector calculate(size_t N) {
        BmpVector ck = { 1.0 / 2 };
        for (size_t n = 1; n < N; ++n) {
            BmpReal sum = 0;
            for (size_t p = 0; p <= n; ++p) {
                sum += b_mhalf.at(p)*b_mhalf.at(n - p);
            }
            BmpReal result = ((n + 1)*ck.at(n - 1) + sum) / (n + 2);
            ck.push_back(result);
        }
        return ck;
    }
